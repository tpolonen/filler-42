#include "filler.h"

void	*xalloc(size_t min_size)
{
	size_t	size;
	void	*alloc;

	size = 2;
	while (size < min_size)
		size *= 2;
	alloc = malloc(size);
	if (alloc)
		ft_bzero(alloc, size);
	return (alloc);
}
